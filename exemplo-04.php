<?php

$a = 30;

$b = 55.0;
// maior que
var_dump($a > $b);
echo "<br >";

// menor que
var_dump($a < $b);
echo "<br >";

// atribui o valor
var_dump($a = $b);
echo "<br >";

// comparação de valor 
var_dump($a == $b);
echo "<br >";

// comparação de tipo e valor (se são idênticos)
var_dump($a === $b);
echo "<br >";

// comparação de diferença
var_dump($a != $b);
echo "<br >";

?>